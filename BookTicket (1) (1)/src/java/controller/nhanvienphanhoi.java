    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("nhanvien_ph.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        int passengerId = Integer.parseInt(request.getParameter("passengerid").trim());
        String text = request.getParameter("text");
        Integer employeeID = (Integer) session.getAttribute("employeeid");

        if (employeeID == null) {

            response.sendRedirect("dangnhap.jsp");
            return;
        }
        int repliedStatus = 1;

        DAOEMPLOYEE e = new DAOEMPLOYEE();
        List<Feedback> feedbackid = e.getidnv(passengerId);
        boolean isInserted = e.updateFeedback(text, repliedStatus, employeeID, feedbackid);
        if (isInserted) {
            response.sendRedirect("nhanvien");
        } else {
            request.setAttribute("error", "Lỗi Gửi!");
            request.getRequestDispatcher("nhanvien_ph.jsp").forward(request, response);
        }
    }

